    private void botaoFightMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_botaoFightMouseClicked
        if(personagemEscolhido1 == null || personagemEscolhido2 == null){ 
            avisoEDesc.setForeground(java.awt.Color.RED);
            avisoEDesc.setText("É necessário escolher 2 lutadores para começar a batalha!");
            return; 
        }
        
        clipHino.close();
        clipFighter.close();
        selecaoPersonagem.setVisible(false);
        
        AudioInputStream ais = null;

        try {
            ais = AudioSystem.getAudioInputStream(getClass().getResource("/Sons/musicaBatalha.wav"));
        } catch (UnsupportedAudioFileException ex) {
            Logger.getLogger(FarroupsMonTelaInicial.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(FarroupsMonTelaInicial.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            clipPokemon = AudioSystem.getClip();
        } catch (LineUnavailableException ex) {
            Logger.getLogger(FarroupsMonTelaInicial.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            clipPokemon.open(ais);
        } catch (LineUnavailableException ex) {
            Logger.getLogger(FarroupsMonTelaInicial.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(FarroupsMonTelaInicial.class.getName()).log(Level.SEVERE, null, ex);
        }

        FloatControl gainControl = (FloatControl) clipPokemon.getControl(FloatControl.Type.MASTER_GAIN);
        double gain = 0.05;
        float dB = (float) (Math.log(gain) / Math.log(10.0) * 20.0);
        gainControl.setValue(dB);

        clipPokemon.start();
        clipPokemon.loop(Clip.LOOP_CONTINUOUSLY);
        
        AudioInputStream ais2 = null;
        try {
            ais2 = AudioSystem.getAudioInputStream(getClass().getResource("/Sons/pokeball.wav"));
        } catch (UnsupportedAudioFileException ex) {
            Logger.getLogger(FarroupsMonTelaInicial.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(FarroupsMonTelaInicial.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        if(soundEffects.isOpen()) soundEffects.close();
        
        try {
            soundEffects.open(ais2);
        } catch (LineUnavailableException ex) {
            Logger.getLogger(FarroupsMonTelaInicial.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(FarroupsMonTelaInicial.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        gainControl = (FloatControl) soundEffects.getControl(FloatControl.Type.MASTER_GAIN);
        gain = 0.05;
        dB = (float) (Math.log(gain) / Math.log(10.0) * 20.0);
        gainControl.setValue(dB);
        
        soundEffects.loop(0);
        

        Personagem p1 = null;

        try {
            p1 = new Personagem(personagemEscolhido1);  
        } catch (LineUnavailableException ex) {
            Logger.getLogger(FarroupsMonTelaInicial.class.getName()).log(Level.SEVERE, null, ex);
        }

        Personagem p2 = null;

        try {
            p2 = new Personagem(personagemEscolhido2);
        } catch (LineUnavailableException ex) {
            Logger.getLogger(FarroupsMonTelaInicial.class.getName()).log(Level.SEVERE, null, ex);
        }
       
        this.p1ayer1 = p1;
        this.player2 = p2;       
        
        if(boxBot.isSelected()) {
            this.player2.setIsBot(true);
            bot = new Bot_AI(this.player2, this.p1ayer1);
        }
        
        iniciaCampos();
        
        this.setVisible(false);
    }//GEN-LAST:event_botaoFightMouseClicked

