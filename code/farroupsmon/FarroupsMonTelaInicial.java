    private void botaoPlayMouseEntered(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_botaoPlayMouseEntered
        botaoPlay.setIcon(new ImageIcon(getClass().getResource("/Imagens/playButtonDark.png")));
        euBolaDeTenis.setVisible(true);
        
        AudioInputStream ais = null;

        try {
            ais = AudioSystem.getAudioInputStream(getClass().getResource("/Sons/select.wav"));
        } catch (UnsupportedAudioFileException ex) {
            Logger.getLogger(FarroupsMonTelaInicial.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(FarroupsMonTelaInicial.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        if(soundEffects.isOpen()) soundEffects.close();
        
        try {
            soundEffects.open(ais);
        } catch (LineUnavailableException ex) {
            Logger.getLogger(FarroupsMonTelaInicial.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(FarroupsMonTelaInicial.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        soundEffects.loop(0);
    }//GEN-LAST:event_botaoPlayMouseEntered

